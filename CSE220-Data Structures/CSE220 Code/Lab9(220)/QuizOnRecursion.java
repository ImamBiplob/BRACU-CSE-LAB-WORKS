import java.util.Scanner;
public class QuizOnRecursion {
  public static void main(String[] args) {
    Scanner sc=new Scanner(System.in);
    int a=sc.nextInt();
    printChar(a);
  }
    public static void printChar(int a) {
      if(a==0) return;
     else {
         System.out.println(a%10);
        a=a/10;
        printChar(a);
       
     }
   }
}
